package cnblog.entity;

public class FileData {
String bits;
String name;
String type;

public String getBits() {
    return bits;
}

public void setBits(String bits) {
    this.bits = bits;
}

public String getName() {
    return name;
}

public void setName(String name) {
    this.name = name;
}

public String getType() {
    return type;
}

public void setType(String type) {
    this.type = type;
}
}
